#include <iostream>

int main(){

    // addition
    int students = 20; // int for whole number guide available in variables.cpp

    students = students + 1; // how add works or do students+=1; same thing pretty much just more ugly

    std::cout << students << "\n";


    // subtraction

    students = students - 1; // takes away one or do students-=1;

    std::cout << students << "\n";

    // multiply

    students = students * 2; // * = times or do students*=2;

    std::cout << students << "\n";


    // division

    students = students / 2; // / equals divide or do students/=2;

    std::cout << students << "\n";


    return 0;
}